package Assignments_AB_43;

public class Assignment_01_Add_Multi_sub_div_mod_in_mainmethod {

	public static void main(String[] args) {
		
			int a = 10;
			int b = 20;
			int c = a+b;
			System.out.println(c);
		
			int e = 10;
			int f = 20;
			int g = f-e;
			System.out.println(g);
		
			int i = 2;
			int j = 5;
			int k = i*j;
			System.out.println(k);
		
			int  x= 2;
			int  y= 7;
			int  z= y/x;  //it will always show the quotient - 3
			System.out.println(z);
		
			int  no1= 7;
			int  no2= 2;
			int  mod=no1%no2; //it will always show the reminder 7/2 = quotient - 3, remainder - 1
			System.out.println(mod);
	}
}
